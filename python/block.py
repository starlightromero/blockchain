"""Import time library."""
from time import time
from utility.printable import Printable


class Block(Printable):
    """A Block of Blockchain.

    Attributes
    ----------
        index : str
            The index of this block.
        previous_hash : json
            The hash of the previous block in the blockchain.
        timestamp : time
            The timestamp of the block (automatically generated by default).
        transactions : list
            A list of transaction which are included in the block.
        proof : str
            The proof of work number that yielded this block.

    """

    def __init__(
        self, index, previous_hash, transactions, proof, time=time()
    ):
        self.index = index
        self.previous_hash = previous_hash
        self.timestamp = time
        self.transactions = transactions
        self.proof = proof
